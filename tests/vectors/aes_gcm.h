struct aes_gcm_vector {
	const unsigned char *key;
	unsigned int         key_len;
	const unsigned char *nonce;        /* 12 bytes (96-bit IV) */
	const unsigned char *plaintext;
	unsigned int         pt_len;
	const unsigned char *aad;          /* additional authenticated data */
	unsigned int         aad_len;
	const unsigned char *ciphertext;   /* same length as plaintext */
	const unsigned char *tag;
	unsigned int         tag_len;      /* 12 or 16 bytes */
	const char          *source;
};

