/*
 * aes_gcm.h — AES-GCM known-answer test vectors
 *
 * Source: wolfSSL wolfcrypt/test/test.c aesgcm_test() / aesgcm_default_test(),
 *   which cites NIST SP 800-38D and McGrew-Viega "The Galois/Counter Mode of
 *   Operation" (2004) test vectors.
 *
 * Verified against gogood-wolfssl wolfcrypt/aesgcm/aesgcm_test.go.
 * Do NOT modify these values. Do NOT generate vectors from the code under test.
 *
 * FIPS 140-3 note: NIST SP 800-38D mandates a minimum 96-bit (12-byte)
 * authentication tag for FIPS-approved use. tag_len field specifies the
 * authentication tag length in bytes; all vectors use tag_len of 12 or 16.
 */
