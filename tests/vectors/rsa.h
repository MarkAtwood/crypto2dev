/*
 * Offsets to extract PKCS#1 format keys from the DER blobs above.
 *
 * rsa2048_priv_der is PKCS#8 (PrivateKeyInfo).  The kernel crypto API for RSA
 * (wc_RsaPrivateKeyDecode in wolfcrypt.ko) expects the inner RSAPrivateKey DER
 * (PKCS#1 format, starting with SEQUENCE).  The inner key begins at byte 26:
 *   PKCS#8 header (4) + version (3) + AlgorithmIdentifier SEQUENCE (2+9+2=13)
 *   + OCTET STRING header (4) = 24 ... wait, recount:
 *   outer SEQ hdr  : 30 82 04 be  = 4 bytes
 *   version        : 02 01 00     = 3 bytes
 *   AlgId SEQ      : 30 0d        = 2 bytes
 *   OID rsaEnc     : 06 09 ...    = 11 bytes
 *   NULL           : 05 00        = 2 bytes
 *   OCTET STRING   : 04 82 04 a8  = 4 bytes  (2-byte length)
 *                    total        = 26 bytes
 *
 * rsa2048_pub_der is SubjectPublicKeyInfo.  wc_RsaPublicKeyDecode accepts
 * SubjectPublicKeyInfo directly, so no offset is needed for the public key.
 * The RSAPublicKey (PKCS#1 pub) begins at byte 24 if needed separately:
 *   SPKI SEQ hdr   : 30 82 01 22  = 4 bytes
 *   AlgId SEQ      : 30 0d        = 2 bytes
 *   OID rsaEnc     : 06 09 ...    = 11 bytes
 *   NULL           : 05 00        = 2 bytes
 *   BIT STRING hdr : 03 82 01 0f  = 4 bytes
 *   BIT STRING pad : 00           = 1 byte
 *                    total        = 24 bytes
 */
#define RSA2048_PKCS1_PRIV_OFFSET  26
#define RSA2048_PKCS1_PRIV_LEN     (rsa2048_priv_der_len - RSA2048_PKCS1_PRIV_OFFSET)

